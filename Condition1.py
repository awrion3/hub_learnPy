score = float(input("Your Grade: "))

if score == 4.5:
    print('A+')
elif score >= 4:
    print('A0')
elif score >= 3.5:
    print('B+')
elif score >= 3:
    print('B0') 
elif score >= 2.5:
    print('C+')
elif score >= 2:
    print('C0')
elif score >= 1.5:
    print('D+')
elif score >= 1:
    print('D0')
else: 
    print('F')